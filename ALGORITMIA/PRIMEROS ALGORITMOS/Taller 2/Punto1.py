#1)	Se requiere conocer el área de un rectángulo. Realice un algoritmo para tal fin
#            El área del rectángulo se halla así: (base*altura)

print("Programa area del rectangulo")

base = int(input("Introduce la base del rectangulo "))

altura =int(input("Introduce la altura del rectangulo "))

area=base*altura

print("El area del rectangulo es " + str(area))
